class Solution {
    public int[] getLeastNumbers(int[] arr, int k) {
        if( k < 1){
            return new int[k];
        }
        PriorityQueue<Integer> maxHeap = new PriorityQueue<>(k, new Comparator<Integer>() {
            public int compare(Integer o1 , Integer o2){
                return o1 - o2;
            }
        });

        for( int num : arr){
            maxHeap.add(num);
        }

        int[] res = new int[k];
        for (int i = 0; i < k; i++) {
            res[i] = maxHeap.poll();
        }
        return res;
    }
}
