/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */
class Solution {
    public int kthLargest(TreeNode root, int k) {
        Stack<TreeNode> s = new Stack<>();
        int count = 0;
        TreeNode temp = root;
        while(temp != null || !s.empty()){
            while(temp != null){
                s.push(temp);
                temp = temp.right;
            }
            temp = s.pop();
            count++;
            if(count == k){
                return temp.val;
            }
            temp = temp.left;
        }
        return -1;
    }
}
