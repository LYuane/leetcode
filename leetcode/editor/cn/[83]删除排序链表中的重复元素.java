/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    public ListNode deleteDuplicates(ListNode head) {
        Set<Integer> set = new HashSet<>();

        if(head == null || head.next == null){
            return head;
        }

        ListNode pre = head;
        ListNode curr = head.next;
        int sign = pre.val;
        while(curr != null){
            if(curr.val == sign){
                curr = curr.next;
            }else{
                pre.next = curr;
                pre = curr;
                sign = pre.val;
                curr = curr.next;
            }
        }
        pre.next = null;
        return head;
    }
}
